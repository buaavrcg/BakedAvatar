def ensure_dir(path, show_info=True):
    """Create path by first checking its existence."""
    if not os.path.exists(path):
        if show_info:
            print("Create folder", path)
        os.makedirs(path, exist_ok=True)
    else:
        if show_info:
            print(path, "already exists.")


def find_latest_model_path(dirname, prefix=None, ext=None):
    """
    Find latest path (with largest postfix) in a directory with prefix and ext.
    Returns: The latest path. None if not exists.
    """
    if not os.path.exists(dirname):
        return None

    model_list = [
        os.path.join(dirname, f) for f in os.listdir(dirname)
        if os.path.exists(os.path.join(dirname, f)) \
            and (prefix is None or f.startswith(prefix)) \
            and (ext is None or f.endswith(ext))
    ]
    if len(model_list) == 0:
        return None

    model_list.sort(key=lambda f: int(''.join(filter(str.isdigit, f))))
    return model_list[-1]
